    <h1>Pruebas de Integración</h1>

    <h2>¿Qué son las Pruebas de Integración?</h2>
    <p>
        Las pruebas de integración son un tipo de prueba de software que se realizan 
        después de las pruebas unitarias. Su objetivo es verificar que los diferentes 
        módulos o componentes del sistema funcionen correctamente cuando trabajan juntos.
    </p>

    <h2>Objetivo</h2>
    <p>
        El objetivo principal es detectar errores en la comunicación entre módulos, 
        en el intercambio de datos y en la interacción entre las diferentes partes del sistema.
    </p>

    <h2>¿Cómo se realizan?</h2>
    <p>
        Primero se prueban los módulos de manera individual (pruebas unitarias). 
        Luego se van uniendo progresivamente para comprobar que la integración 
        entre ellos no genere errores.
    </p>

    <h2>Tipos de Integración</h2>
    <ul>
        <li><strong>Integración Incremental:</strong> Los módulos se integran poco a poco y se prueban en cada paso.</li>
        <li><strong>Integración Big Bang:</strong> Todos los módulos se integran al mismo tiempo y luego se prueban juntos.</li>
        <li><strong>Integración Top-Down:</strong> Se comienza probando los módulos superiores y luego los inferiores.</li>
        <li><strong>Integración Bottom-Up:</strong> Se empieza por los módulos más básicos y luego se integran los superiores.</li>
    </ul>

    <h2>Importancia</h2>
    <p>
        Las pruebas de integración son importantes porque permiten detectar fallos 
        que no se identifican en las pruebas unitarias, asegurando que el sistema 
        funcione de manera correcta como un conjunto.
    </p>

