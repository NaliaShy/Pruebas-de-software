    <h1>Pruebas Unitarias</h1>

    <h2>¿Qué son las Pruebas Unitarias?</h2>
    <p>
        Las pruebas unitarias son un tipo de prueba de software que se enfocan en verificar 
        el correcto funcionamiento de las partes más pequeñas del sistema, como funciones, 
        métodos o clases individuales.
    </p>

    <h2>Objetivo</h2>
    <p>
        El objetivo principal de las pruebas unitarias es detectar errores en etapas tempranas 
        del desarrollo, asegurando que cada componente funcione correctamente de manera independiente.
    </p>

    <h2>¿Cómo se realizan?</h2>
    <p>
        Se crean casos de prueba específicos para cada unidad del sistema, donde se definen 
        datos de entrada y resultados esperados. Luego se ejecutan estas pruebas para comprobar 
        que la salida obtenida coincida con lo esperado.
    </p>

    <h2>Ventajas</h2>
    <ul>
        <li>Permiten detectar errores rápidamente.</li>
        <li>Facilitan el mantenimiento del código.</li>
        <li>Mejoran la calidad del software.</li>
        <li>Ayudan a prevenir fallos en etapas posteriores.</li>
    </ul>

    <h2>Importancia</h2>
    <p>
        Las pruebas unitarias son fundamentales en el desarrollo de software, ya que 
        garantizan que cada parte del sistema funcione correctamente antes de integrarse 
        con otros módulos.
    </p>

